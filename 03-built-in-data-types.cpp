#include <iostream>
using namespace std;
/*
Here we define all built-in data types in cpp.
Time = O(1), Space = O(1).
*/
int main()
{
    int a = 4, b = 5;
    float pi = 3.15;
    char c = 'A';
    double d = 3.14213432123422123;
    bool man = true;
    cout << "Value of integer a = " << a << endl;
    cout << "Value of integer b = " << b << endl;
    cout << "Value of float pi = " << pi << endl;
    cout << "Value of char c = " << c << endl;
    cout << "Value of double d = " << d << endl;
    cout << "Value of bool men = " << man << endl;
    return 0;
}